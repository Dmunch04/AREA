// Settings data class
public class SettingsData
{

    public int GraphicsIndex = 0;       // The index of graphics quality
    public bool Music = true;           // A bool for whether music should be on or not
    public bool Sounds = true;          // A bool for whether sounds should be on or not
    public float MusicVolume = 100f;    // The volume of the music
    public float SoundsVolume = 100f;   // The volume of the sounds 
    public bool AutoReload = false;     // A bool for whether it should auto reload or not
    public bool GameStats = false;      // A bool for whether it should show the game stats or not

}